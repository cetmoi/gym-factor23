export const getExerciseById = (id: number) => {
  return EXERCISES_DATABASE.find(exercise => exercise.id === id);
};

export const getExercisesByIds = (ids: number[]) => {
  return ids.map(id => getExerciseById(id)).filter(exercise => exercise !== undefined);
};